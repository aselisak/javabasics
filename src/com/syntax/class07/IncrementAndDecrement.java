		int num=10;
		
		num=num+1;
		
		num+=1;
		
		num++;
		
		System.out.println(num);// increment operator add 1 to a variable
		
		
		num++;
		System.out.println(num);
		//10++ increment doesn't work with any value Invalid argument to operation ++/--
		// Decrement operator subtract 1 from a variable
		
		num--;
