package com.syntax.class08;

public class AnotherExampleNestedLoops {

	public static void main(String[] args) {
	System.out.println("-----ANOTHER EXAMPLE-------");
	
	for(int i=0; i<=9; i++) {
		
		for(int a=0; a<=9; a++ ) {
			
			for(int b=0; b<=9; b++) {
				System.out.println(i+" "+a+ " "+b);
			}
		}
	}
	
	
	
	
	
	
	}

}
